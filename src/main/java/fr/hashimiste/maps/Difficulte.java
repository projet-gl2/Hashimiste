package fr.hashimiste.maps;

/**
 * L'énumération Difficulte représente les niveaux de difficulté d'un jeu ou d'une tâche.
 * Elle a trois niveaux : FACILE, MOYEN et DIFFICILE.
 *
 * Cela peut être utilisé pour définir le niveau de difficulté d'un jeu ou d'une tâche.
 */
public enum Difficulte {
    /**
     * Représente un niveau de difficulté facile.
     */
    FACILE,

    /**
     * Représente un niveau de difficulté moyen.
     */
    MOYEN,

    /**
     * Représente un niveau de difficulté difficile.
     */
    DIFFICILE
}
